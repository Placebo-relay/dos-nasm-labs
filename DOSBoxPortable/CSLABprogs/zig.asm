org 0x100

; Переводим консоль в графический режим
mov ah, 0x00          ; Функция BIOS выбора графического режима
mov al, 0x13          ; Графический режим 320x200, 256 цветов
int 0x10             ; Вызов BIOS

; Устанавливаем цвет
mov ah, 0x0C         ; Функция BIOS отображения точки
mov al, 0x04         ; Задание цвета (красный)

; Рисуем первую линию (от (0, 0) до (319, 199))
mov cx, 0            ; Начальная x-координата
mov dx, 0            ; Начальная y-координата
draw_line_1:
    ; Рисуем точку
    int 0x10          ; Вызов BIOS
    inc cx            ; Увеличиваем x
    inc dx            ; Увеличиваем y
    cmp cx, 199       ; Проверяем, не достигли конца по x
    jl draw_line_1    ; Если нет, продолжаем рисовать

; Рисуем вторую линию (от (320, 0) до (0, 199))
mov cx, 319          ; Начальная x-координата (максимум)
mov dx, 0            ; Начальная y-координата
draw_line_2:
    ; Рисуем точку
    int 0x10          ; Вызов BIOS
    dec cx            ; Уменьшаем x
    inc dx            ; Увеличиваем y
    cmp dx, 200       ; Проверяем, не достигли конца по y
    jl draw_line_2    ; Если нет, продолжаем рисовать

; Ожидание нажатия клавиши
nokey:
mov ah, 0x01         ; Функция чтения клавиатуры
int 0x16             ; Вызов BIOS
jz nokey             ; Переходим, если ничего не прочитано

; Возвращаемся в текстовый режим
mov ah, 0x00         ; Функция BIOS выбора графического режима
mov al, 0x03         ; Текстовый режим 80x25, 16 цветов
int 0x10             ; Вызов BIOS

ret
